/*
Variable in JS are containers which store some values
- These are dynamically typed which means we do not define the 
type of variable it decide it while runtime
*/
// Dynamically typed example
var vari = 100; // vari is storing an 100 which is an integer
vari = "Prince" // vari is now storing "Prince" which is an string
// The above change take place during runtime

